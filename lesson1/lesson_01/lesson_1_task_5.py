# Определяем функцию, которая принимает на вход одну цифру и выводит её в консоль
def print_digit(digit):
    print(digit, end="")  # Используем end="", чтобы цифры выводились слитно

# Номер телефона, который будем выводить по частям
phone_number = "88005553535"

# Вызываем функцию 11 раз, передавая каждую цифру из номера телефона
for digit in phone_number:
    print_digit(digit)

# Переходим на новую строку после вывода всего номера
print()  # Это добавлено для удобства, чтобы следующая команда вывода начиналась с новой строки
